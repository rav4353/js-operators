document.getElementById("plus").innerHTML = z;

// -

let a = 5;
let b = 2;
let c = a - b;
document.getElementById("minus").innerHTML = c;

// *

let d = 5;
let e = 2;
let f = d * e;
document.getElementById("multiply").innerHTML = f;

// /

let g = 5;
let h = 2;
let i = g / h;
document.getElementById("divide").innerHTML = i;

// Exponentiation **

let j = 5;
